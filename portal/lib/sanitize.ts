/**
 * Escape HTML special characters to prevent XSS when interpolating
 * user input into HTML strings (email templates, HubSpot notes, etc.)
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}
